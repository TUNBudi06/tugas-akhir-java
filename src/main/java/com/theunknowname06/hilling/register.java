    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        tPass = password.getText();
        tName = name.getText();
        tEmail = email.getText();
        String token = codeVerify.getText();
        if (tName.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter your Name.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (tEmail.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter your Email.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (tPass.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter your password.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
         if (!token.equals(c_ram)){
            javax.swing.JOptionPane.showMessageDialog(this, "Code verification wrong!!!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
    showMessageDialog("Registration successful!", "Success", javax.swing.JOptionPane.NO_OPTION);
    this.dispose();
    login pg_login = new login(tName,tPass,tEmail);
        pg_login.show_login();
    }//GEN-LAST:event_registerActionPerformed


