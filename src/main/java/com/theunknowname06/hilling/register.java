    public static boolean sendVerificationEmail(String email, String codeVerify) {
    // SMTP server properties
    RECIPIENT_EMAIL = email;
    VERIFICATION_CODE = codeVerify;
    Properties properties = new Properties();
    properties.put("mail.smtp.host", HOST);
    properties.put("mail.smtp.port", PORT);
    properties.put("mail.smtp.auth", "true");
    properties.put("mail.debug", "false"); 
    properties.put("mail.smtp.starttls.enable", "true");
    properties.put("mail.smtp.starttls.required", "true");
    properties.put("mail.smtp.ssl.protocols", "TLSv1.2");
    properties.put("mail.smtp.starttls.enable", "true");
    properties.put("mail.smtp.socketFactory.port", "465");
    properties.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
    properties.put("mail.smtp.socketFactory.fallback", "false");
    properties.put("mail.smtp.ssl.enable", "true");

    // Create a session with authentication
    Authenticator authenticator = new Authenticator() {
        @Override
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(USERNAME, PASSWORD);
        }
    };
    Session session = Session.getInstance(properties, authenticator);

    try {
        // Create an email message
        Message message = new MimeMessage(session);
        message.setFrom(new InternetAddress(USERNAME));
        message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(RECIPIENT_EMAIL));
        message.setSubject(SUBJECT);
        message.setText("Please use the following verification code: " + VERIFICATION_CODE);

        // Send the email
        Transport.send(message);
        return true;
    } catch (MessagingException e) {
        return false;
    }
    }

